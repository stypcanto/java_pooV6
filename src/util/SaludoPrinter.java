// Paquete donde se encuentra la clase, útil para organizar el código
package util;

// Declaración de la clase SaludoPrinter
public class SaludoPrinter {

    // Método público que imprime un mensaje de saludo recibido como parámetro
    public void imprimirSaludo(String mensaje) {
        // Muestra el mensaje en consola
        System.out.println(mensaje);
    }

    // Método público que imprime una línea separadora visual
    public void imprimirSeparador() {
        // Imprime una línea de guiones como separador visual
        System.out.println("---------------------");
    }
}
